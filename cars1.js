function display(cars)
{
    document.querySelector("#productsbox").innerHTML="";
    cars.map(function(elem)
    {
    let div=document.createElement("div");
    let div1=document.createElement("div");
    let img=document.createElement("img");
    img.src=elem.img;
    let p1=document.createElement("p");
    p1.innerText=elem.price;
    let p2=document.createElement("p");
    p2.innerText=elem.model;
    let p3=document.createElement("p");
    p3.innerText=elem.desc;
    
    let p4=document.createElement("p");
    p4.innerText=elem.location;
    let span=document.createElement("span");
    span.innerText=elem.date;
    p4.append(span);
    div1.append(img);
    div.append(div1,p1,p2,p3,p4);
    document.querySelector("#productsbox").append(div);
});

}

document.querySelector("#sort").addEventListener("change", sortFunc);
function sortFunc()
{
    let selected=document.querySelector("#sort").value;
    
    if(selected=="lth")
    {
        cars.sort(function(a,b)
        {
            let x=Number(a.rupees);
            let y=Number(b.rupees);
            if(x>y) return 1;
            if(x<y) return -1;
            return 0;
        });
        display(cars);
    }
    else if(selected=="htl")
    {
        cars.sort(function(a,b)
        {
            let x=Number(a.rupees);
            let y=Number(b.rupees);
            if(x>y) return -1;
            if(x<y) return 1;
            return 0;
        });
        display(cars);  
    }
    else if(selected=="distance")
    {
        cars.sort(function(a,b)
        {
            let x=Number(a.km);
            let y=Number(b.km);
            if(x>y) return 1;
            if(x<y) return -1;
            return 0;
        });
        display(cars);    
    }
    else if(selected=="")
    {
        display(cars);
    }
    
}

